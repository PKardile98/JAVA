package Array;

public class Array7 {

	public static void main(String[] args) {
		//Reverse Printing No.
		
		int A3[]={1,2,3,4,5,6,7,8,9};

		System.out.println(A3.length);


		for(int i=0; i<A3.length; i++){

		   System.out.print(A3[i]+" ");

		}

		 System.out.println();

		for(int j=A3.length-1; j>=0; j--)
		{

		   System.out.print(A3[j]+" ");

		}


	}

}
