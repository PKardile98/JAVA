package DataTypes;

public class Conditionaldatatypes {

	public static void main(String[] args)
	{
		// Boolean Data Types--- 1 bit 
		// true and false condition
		
		boolean B1= true;
		boolean B2= false;
		
		System.out.println("Conditional Data Type:");
		System.out.println("Boolean Data Type--->"+B1);
		System.out.println("Boolean Data Type--->"+B2);
		
		

	}

}
