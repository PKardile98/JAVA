package DataTypes;

public class NumericsandDecimal {

	public static void main(String[] args)
	{
		// Only float and double 
		// Float --- 4 byte--> 32bits
		
		float F1= 45.59f; // 'f' is compulsory 
		float F2= 78.67f;
		
		float F3= -78.68f;
		float F4= -8958.78f;
		
		System.out.println("Float Data Type----->"+F1);
		System.out.println("Float Data Type----->"+F2);
		System.out.println();
		System.out.println("Float Data Type----->"+F3);
		System.out.println("Float Data Type----->"+F4);
		
		System.out.println("**************************************************");
		
		
		// Double--8 bytes--> 64bits 
		
		double D1= 7577.567d; // "d" Tere man pe hai 
		double D2= 747646.9697d;
		
		double D3= -856769.6d;
		double D4= -7566.85d;
		
		System.out.println("Double Data Types------>"+D1);
		System.out.println("Double Data Types------>"+D2);
		System.out.println();
		System.out.println("Double Data Types------>"+D3);
		System.out.println("Double Data Types------>"+D4);
		
	}

}
