package Loopstatement;

public class loop1For {

	public static void main(String[] args) {
		// Syntax                                                
		//for(initialization ;condition ; increment/decrement) {    
			//block of statements    
		//	}
		
		// printing Number from 1-10
		
		for(int i=1; i<=11; i++) {
			
			System.out.println(i);
			
		}
		
		System.out.println("Loop ended:");

	}

}
